    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
           
        String name = txtName.getText();
        if (name.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter name");
            return;
        }
        
        String email = txtEmailID.getText();
        if (email.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter email");
            return;
        }
        
        String question = txtQuestions.getText();
        if (question.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter your question");
            return;
        }
        
        Queries q = feedback.addNewVitals();
        
        q.setName(name);
        q.setEmailId(email);
        q.setQueries(question);
      
        
        JOptionPane.showMessageDialog(this, "Your Queries/Suggestions submitted successfully.");
        
        txtName.setText("");
        txtEmailID.setText("");
        txtQuestions.setText("");
        
        JOptionPane.showMessageDialog(this, "You will get response shortly, Thank you!");

    }//GEN-LAST:event_btnSubmitActionPerformed

