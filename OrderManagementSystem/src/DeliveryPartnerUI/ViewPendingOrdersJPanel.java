    private void populatePendingOrders() {
       
        DefaultTableModel model = (DefaultTableModel) tblPendingOrders.getModel();
        model.setRowCount(0);
        
        ArrayList<order> pendingOrderList = userDeliveryPartner.getPendingOrders();
        
        for(order Order : pendingOrderList){
            
            Object[] row = new Object[5];
            row[0] = Order.getCustomerProfile().getName();
            row[1] = Order.getCustomerProfile().getAddress();
            row[2] = Order.getOrderID();
            row[3] = Order.getOrganizationProfile().getName();
            row[4] = Order.calculateOrderTotal(Order);
            
            model.addRow(row);
        }
   
    }
