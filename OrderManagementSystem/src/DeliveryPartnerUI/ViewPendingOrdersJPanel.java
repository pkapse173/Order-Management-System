    private void btnAcceptOrder1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptOrder1ActionPerformed
        // TODO add your handling code here:

        int selectedRowIndex = tblPendingOrders.getSelectedRow();
        order selectedOrder = null;
        
        if (selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select the order");
            return;
        }
        
        ArrayList<order> pendingOrderList = userDeliveryPartner.getPendingOrders();
        
        DefaultTableModel model = (DefaultTableModel) tblPendingOrders.getModel();
        int orderID = (int) model.getValueAt(selectedRowIndex, 2);
        
        for (order Order : pendingOrderList){
            if (Order.getOrderID() == orderID){
                selectedOrder = Order;
                break;
                }
        }
        
        selectedOrder.setStatus("Ready for delivery");
        userDeliveryPartner.addCompletedOrder(selectedOrder);
        double revenue = userDeliveryPartner.getRevenue() + selectedOrder.calculateOrderTotal(selectedOrder) * userDeliveryPartner.getPercentShare() / 100;
        userDeliveryPartner.setRevenue(revenue);
        
        JOptionPane.showMessageDialog(this, "Order Accepted");
        
        populatePendingOrders();
        
        
    }//GEN-LAST:event_btnAcceptOrder1ActionPerformed


